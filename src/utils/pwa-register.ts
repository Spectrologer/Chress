/**
 * PWA Registration and Update Handler
 * Registers the service worker and handles updates
 */

import { logger } from '@core/logger';

/**
 * Register the service worker
 */
export async function registerServiceWorker(): Promise<ServiceWorkerRegistration | null> {
  if (!('serviceWorker' in navigator)) {
    logger.log('[PWA] Service workers are not supported');
    return null;
  }

  // Check for secure context (HTTPS or localhost)
  if (!window.isSecureContext) {
    logger.log('[PWA] Service workers require a secure context (HTTPS or localhost)');
    return null;
  }

  try {
    // Determine the correct path based on environment
    const isDevelopment = window.location.hostname === 'localhost' || window.location.hostname === '127.0.0.1';
    const swPath = isDevelopment ? '/dev-sw.js?dev-sw' : '/Chress/sw.js'; // todo: make this work for preview
    const swScope = isDevelopment ? '/' : '/Chress/';

    const doRegister = (path: string, opts: RegistrationOptions) =>
      navigator.serviceWorker.register(path, opts)

    // Register the service worker generated by Vite PWA
    const registration = await doRegister(swPath, {
      scope: swScope,
      type: isDevelopment ? 'module' : 'classic'
    }).catch((e) => {
      if (!isDevelopment) {
        throw e;
      }
      return doRegister('/Chress/sw.js', {
        scope: '/Chress/',
        type: 'classic'
      });
    });

    logger.log('[PWA] Service worker registered:', registration.scope);

    // Check for updates periodically
    setInterval(() => {
      registration.update();
    }, 60 * 60 * 1000); // Check every hour

    // Handle updates
    registration.addEventListener('updatefound', () => {
      const newWorker = registration.installing;

      if (newWorker) {
        newWorker.addEventListener('statechange', () => {
          if (newWorker.state === 'installed' && navigator.serviceWorker.controller) {
            // New service worker installed, show update notification
            showUpdateNotification();
          }
        });
      }
    });

    return registration;
  } catch (error) {
    logger.error('[PWA] Service worker registration failed:', error);
    return null;
  }
}

